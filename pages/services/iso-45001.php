              <h3 class="column-title-small">Training Background</h3>
              <p>ISO 45001 will replace OHSAS 18001 for three years after its publication in March 2018. BSI will officially withdraw BS OSHAS 18001 in March 2021, at the end of the migration period. ISO 45001 uses the Annex SL management system standard structure guidelines to allow for simplified integration with other management system standards, such as ISO 9001 and ISO 14001. The International Accreditation Forum has published requirements for migration from OHSAS 18001 to ISO 45001.</p>
              <p>ISO 45001 is an international standard that sets standards or requirements for occupational health and safety / OHS management systems, with guidelines for their use, which enables an organization to proactively improve OHS Management System performance in preventing occupational injuries and illnesses and diseases.</p>
              <p>ISO 45001 is intended to be applicable to any organization regardless of size, type and nature. All requirements are intended to be integrated into the organization's own management processes, ISO 45001 allows an organization to be able to integrate with other management systems, such as ISO 9001, ISO 14001, and others.</p>
