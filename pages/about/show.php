          <h3 class="column-title">What Is A Standard?</h3>
          <p>In essence, a standard is an agreed way of doing something. It could be about making a product, managing a process, delivering a service or supplying materials – standards can cover a huge range of activities undertaken by organizations and used by their customers Standards are the distilled wisdom of people with expertise in their subject matter and who know the needs of the organisations they represent – people such as manufacturers, sellers, buyers, customers, trade associations, users or regulators.</p>
          
          <p>Our portfolio extends to more than 30,000 current standards. They are designed for voluntary use so it’s up to you – you’re not forced to follow a set of rules that make life harder for you, you’re offered ways to do your work better. Standards are knowledge. They are powerful tools that can help drive innovation and increase productivity. They can make organisations more successful and people’s everyday lives easier, safer and healthier.</p>
